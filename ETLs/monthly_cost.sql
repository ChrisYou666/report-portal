SELECT 
A.BUKRS AS COMPANY_CODE,
A1.BUTXT AS COMPANY_NAME,
MAND1.DDTEXT AS COMPANY_NAME_MAND,
B.ESTNR AS ESTATE_CODE,
A.PRFNR AS ESTATE_NAME,
MAND2.DDTEXT AS ESTATE_NAME_MAND,
REG.RGNNR AS REGION_CODE,
REG.NAME1 AS REGION_NAME,
MAND3.DDTEXT AS REGION_NAME_MAND,
C.DIVNR AS DIVISION_CODE,
C.NAME1 AS DIVISION_NAME,
D.BLOCK AS BLOCK_CODE,
D.BNAME AS BLOCK_NAME,
D.SEEDO AS BIBIT,
D.YPLAN AS TAHUN_TANAM,
CPA.SPMON AS PERIODE,
'AB' AS CATATAN,
'ACTUAL_COST' AS MEASURE_NAME,
CPA.SUB_KSTAR as measure_desc,
CASE WHEN CPA.KSTAR = 'WD' THEN 'WEEDING' --WD
WHEN CPA.KSTAR = 'RD' THEN 'ROADS/PATHS/BRIDGES' --RD
WHEN CPA.KSTAR = 'DR' THEN 'DRAINS' --DR
WHEN CPA.KSTAR = 'SW' THEN 'SOIL/WATER CONSERVATION' --SW
WHEN CPA.KSTAR = 'BS' THEN 'BOUNDARIES AND SURVEY' --BS
WHEN CPA.KSTAR = 'CS' THEN 'CENSUS/THINNING/SUPPLYING' --CS
WHEN CPA.KSTAR = 'PR' THEN 'PRUNING & SANITATION' --PR
WHEN CPA.KSTAR = 'PD' THEN 'PESTS & DISEASES' --PD
WHEN CPA.KSTAR = 'MA' THEN 'MANURING' -- MA
WHEN CPA.KSTAR = 'HV' THEN 'HARVESTING LABOURS' --HV
WHEN CPA.KSTAR = 'HT' THEN 'HARVESTING TOOLS' --HT
WHEN CPA.KSTAR = 'TH' THEN 'TRANSPORTS OF HARVESTERS' --TH
WHEN CPA.KSTAR = 'FD' THEN 'FFB DISPATCH' --FD
WHEN CPA.KSTAR = 'SU' THEN 'SUPERVISION' --SU
END AS MEASURE_TEXT,
CPA.COSTWTG*100 AS MEASURE_VALUE,
CPA.KSTAR AS ACTIVITY_WORKGROUP
FROM STG_ZPAY_PROFILE A
LEFT JOIN STG_T001 A1
ON A.BUKRS = A1.BUKRS
LEFT JOIN STG_ZEST_ESTATE B
ON A.BUKRS = B.BUKRS
AND RIGHT(TRIM(A.WERKS),2) = B.ESTNR
LEFT JOIN STG_ZEST_REGION REG
ON B.RGNNR = REG.RGNNR
LEFT JOIN STG_DD07T MAND1
on A.BUKRS = MAND1.DOMVALUE_L
and TRIM(MAND1.DOMNAME) = 'ZDMCOMPANY'
LEFT JOIN STG_DD07T MAND2
on B.WERKS = MAND2.DOMVALUE_L
and TRIM(MAND2.DOMNAME) = 'ZDMESTATE'
left join STG_DD07T MAND3
on REG.RGNNR = MAND3.DOMVALUE_L
and TRIM(MAND3.DOMNAME) = 'ZDMREGION'
LEFT JOIN STG_ZEST_DIVISION C
ON B.ESTNR = C.ESTNR
AND A.BUKRS = C.BUKRS
AND C.KDATE = '9999-12-31'
LEFT JOIN STG_ZEST_BLOCK D
ON D.BUKRS = A.BUKRS
AND B.ESTNR = D.ESTNR
AND C.DIVNR = D.DIVNR
INNER JOIN (SELECT OBJNR, KSTAR,SUB_KSTAR, CASE WHEN COSTPER = 'JAN' THEN CONCAT(GJAHR, '01') 
WHEN COSTPER = 'FEB' THEN CONCAT(GJAHR, '02') 
WHEN COSTPER = 'MAR' THEN CONCAT(GJAHR, '03') 
WHEN COSTPER = 'APR' THEN CONCAT(GJAHR, '04') 
WHEN COSTPER = 'MAY' THEN CONCAT(GJAHR, '05') 
WHEN COSTPER = 'JUN' THEN CONCAT(GJAHR, '06') 
WHEN COSTPER = 'JUL' THEN CONCAT(GJAHR, '07') 
WHEN COSTPER = 'AUG' THEN CONCAT(GJAHR, '08') 
WHEN COSTPER = 'SEP' THEN CONCAT(GJAHR, '09') 
WHEN COSTPER = 'OCT' THEN CONCAT(GJAHR, '10') 
WHEN COSTPER = 'NOV' THEN CONCAT(GJAHR, '11') 
WHEN COSTPER = 'DEC' THEN CONCAT(GJAHR, '12')
END AS SPMON, COSTPER, COSTWTG
FROM (SELECT TRIM(OBJNR) OBJNR, GJAHR,  
CASE 
WHEN KSTAR BETWEEN '5101010101' AND '5101010504' THEN 'WD'
WHEN KSTAR BETWEEN '5101010601' AND '5101011504' THEN 'RD'
WHEN KSTAR BETWEEN '5101011601' AND '5101011704' THEN 'DR'
WHEN KSTAR BETWEEN '5101011801' AND '5101012201' THEN 'SW'
WHEN KSTAR BETWEEN '5101012301' AND '5101012504' THEN 'BS'
WHEN KSTAR BETWEEN '5101012601' AND '5101012804' THEN 'CS'
WHEN KSTAR BETWEEN '5101012901' AND '5101013004' THEN 'PR'
WHEN KSTAR BETWEEN '5101013101' AND '5101013904' THEN 'PD'
WHEN KSTAR BETWEEN '5101020101' AND '5101020804' THEN 'MA'
WHEN KSTAR BETWEEN '5101030101' AND '5101030301' THEN 'HV'
WHEN KSTAR = '5101031002' THEN 'HT'
WHEN KSTAR BETWEEN '5101031103' AND '5101031104' THEN 'TH'
WHEN KSTAR BETWEEN '5101030401' AND '5101030704' THEN 'FD'
WHEN KSTAR BETWEEN '5101030801' AND '5101030901' THEN 'SU'
END AS KSTAR, 
CASE 
WHEN KSTAR BETWEEN '5101010101' AND '5101010104' THEN 'MANYAL CIRCLE WEEDING'
WHEN KSTAR BETWEEN '5101010111' AND '5101010114' THEN 'SELECTIVE WEEDING'
WHEN KSTAR BETWEEN '5101010121' AND '5101010123' THEN 'DECREEPING'
WHEN KSTAR BETWEEN '5101010201' AND '5101010204' THEN 'CPT'
WHEN KSTAR BETWEEN '5101010301' AND '5101010304' THEN 'SPOT SPRAYING'
WHEN KSTAR BETWEEN '5101010321' AND '5101010324' THEN 'Blanket Spraying'
WHEN KSTAR BETWEEN '5101010401' AND '5101010404' THEN 'LALANG CONTROL'
WHEN KSTAR BETWEEN '5101010501' AND '5101010504' THEN 'Slashing'
END AS SUB_KSTAR,
SUM(WTG001) JAN, 
SUM(WTG002) FEB, SUM(WTG003) MAR, SUM(WTG004) APR,
SUM(WTG005) MAY, SUM(WTG006) JUN, SUM(WTG007) JUL,
SUM(WTG008) AUG, SUM(WTG009) SEP, SUM(WTG010) OCT,
SUM(WTG011) NOV, SUM(WTG012) DEC
FROM STG_COSP WHERE WRTTP = '04'
GROUP BY OBJNR,  CASE WHEN KSTAR BETWEEN '5101010101' AND '5101010504' THEN 'WD'
WHEN KSTAR BETWEEN '5101010601' AND '5101011504' THEN 'RD'
WHEN KSTAR BETWEEN '5101011601' AND '5101011704' THEN 'DR'
WHEN KSTAR BETWEEN '5101011801' AND '5101012201' THEN 'SW'
WHEN KSTAR BETWEEN '5101012301' AND '5101012504' THEN 'BS'
WHEN KSTAR BETWEEN '5101012601' AND '5101012804' THEN 'CS'
WHEN KSTAR BETWEEN '5101012901' AND '5101013004' THEN 'PR'
WHEN KSTAR BETWEEN '5101013101' AND '5101013904' THEN 'PD'
WHEN KSTAR BETWEEN '5101020101' AND '5101020804' THEN 'MA'
WHEN KSTAR BETWEEN '5101030101' AND '5101030301' THEN 'HV'
WHEN KSTAR = '5101031002' THEN 'HT'
WHEN KSTAR BETWEEN '5101031103' AND '5101031104' THEN 'TH'
WHEN KSTAR BETWEEN '5101030401' AND '5101030704' THEN 'FD'
WHEN KSTAR BETWEEN '5101030801' AND '5101030901' THEN 'SU'
END,
CASE 
WHEN KSTAR BETWEEN '5101010101' AND '5101010104' THEN 'MANYAL CIRCLE WEEDING'
WHEN KSTAR BETWEEN '5101010111' AND '5101010114' THEN 'SELECTIVE WEEDING'
WHEN KSTAR BETWEEN '5101010121' AND '5101010123' THEN 'DECREEPING'
WHEN KSTAR BETWEEN '5101010201' AND '5101010204' THEN 'CPT'
WHEN KSTAR BETWEEN '5101010301' AND '5101010304' THEN 'SPOT SPRAYING'
WHEN KSTAR BETWEEN '5101010321' AND '5101010324' THEN 'Blanket Spraying'
WHEN KSTAR BETWEEN '5101010401' AND '5101010404' THEN 'LALANG CONTROL'
WHEN KSTAR BETWEEN '5101010501' AND '5101010504' THEN 'Slashing'
END, GJAHR) p
UNPIVOT
(COSTWTG FOR COSTPER IN
(JAN, FEB, MAR, APR,
MAY, JUN, JUL, AUG,
SEP, OCT, NOV, DEC)
)AS example) CPA
ON TRIM(CPA.OBJNR) = CONCAT('KS','JLNG',TRIM(A.BUKRS),'AB',B.ESTNR,C.DIVNR,D.BLOCK)
WHERE left(d.kdate,4)+right(left(d.kdate,7),2) >= CPA.SPMON;